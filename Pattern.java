public class Pattern {

	public static void main(String[] args) {

		System.out.println("the pattern is");
		System.out.println("------------");
	for(int i=1;i<=5;i++)
	{
		for(int j=1;j<=i;j++)
		{
			System.out.print(j+" ");
		}
		System.out.println("\n");
	}
	}

}
